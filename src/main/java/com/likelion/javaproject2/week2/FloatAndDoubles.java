package com.likelion.javaproject2.week2;

public class FloatAndDoubles {
    public static void main(String[] args) {
        float f1 = 3.141592f;
        float f2 = 1.23e10f;

        System.out.printf("%f\n",f1);
        System.out.printf("%f\n",f2);

        double d1 = 3.14;
        double d2 = 1.234567890123456789e16;

        System.out.printf("%f\n",d1);
        System.out.printf("%f\n",d2);
    }
}
