package com.likelion.javaproject2.week2.day4;

public class XorExample {
    public static void main(String[] args) {
        System.out.printf("true XOR true --> %b\n", (true ^ true));
        System.out.printf("true XOR false --> %b\n", (true ^ false));
        System.out.printf("false XOR true --> %b\n", (false ^ true));
        System.out.printf("false XOR false --> %b\n", (false ^ false));
    }
}
